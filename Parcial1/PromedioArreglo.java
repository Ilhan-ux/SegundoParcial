package Parcial1;

/**
 *
 * @author Miguel
 */
public class PromedioArreglo {

    private double num;
    private double prom;

    public PromedioArreglo() {
    }

    public PromedioArreglo(double num, double prom) {
        this.num = num;
        this.prom = prom;
    }

    public double getNum() {
        return num;
    }

    public void setNum(double num) {
        this.num = num;
    }

    public double getProm() {
        return prom;
    }

    public void setProm(double prom) {
        this.prom = prom;
    }
    
    
}
